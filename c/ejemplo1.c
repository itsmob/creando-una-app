// Mostrar información en la consola
#include <stdio.h> // Esta librería permite usar funciones de entrada y salida, como printf()

int main()
{
    // La función printf() muestra texto en la consola

    // Imprime el mensaje "Hola"
    printf("Hola");

    // Imprime un salto de línea para que el siguiente mensaje aparezca en una nueva línea
    printf("\n");

    // Imprime el mensaje "Adios"
    printf("Adios");

    // Imprime "Hola", luego un salto de línea (\n), y luego "Adios"
    printf("Hola\nAdios");

    return 0; // Fin del programa. Devuelve 0 para indicar que todo salió bien.
}