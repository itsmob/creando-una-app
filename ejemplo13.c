// Estructuras de control if/else
#include <stdio.h>

int main(void)
{
    if (4 == 4)
    {
        // En caso de que la condicion sea verdadera, este bloque de codigo se va a ejecutar
        printf("efectivamente 4 es igual a 4\n");
    }
    else
    {
        // En caso de que la condicion sea falsa, se ejecutara este bloque
        printf("parece que la condicion fue falsa\n");
    }

    return 0;
}
